	<h1 class="text-center text-4xl font-bold">Sakk</h1>
	<h2 class="mt-5 mb-3 text-3xl">Szabályok</h2>
	<p>
		A sakk egy kétszemélyes játék, amit egy 8x8-as táblán játszanak. A két játékos a tábla két
		oldalán ül, úgy, hogy a maga szemszögéből a tábla bal alsó sarkában egy sötét mező legyen. A két
		játékosnak különböző színű figurái vannak: egyiknek fehér, másiknak fekete, ez alapján hívjük
		őket fehér játékosnak és fekete játékosnak.
	</p>
	<h3 class="mt-5 mb-3 text-2xl">A figurák elhelyezése</h3>
	<p>
		<span class="font-bold">Királynő: </span>A fehér játékos hozzá leközelebbi sor középső fehér
		cellájára, a fekete játékos a hozzá legközelebbi sor középső fekete cellájára helyezi el.
	</p>
	<p>
		<span class="font-bold">Király: </span>A fehér játékos a királynője jobb oldalára, a fekete
		játékos a királynője bal oldalára helyezi el.
	</p>
	<p>
		<span class="font-bold">Futók (2db): </span>Mindkét játékos a királlyal és királynővel oldalra
		szomszédos cellákba helyezi el.
	</p>
	<p>
		<span class="font-bold">Huszárok (2db): </span>Mindkét játékos a futókkal oldalra szomszédos
		cellákba helyezi el.
	</p>
	<p>
		<span class="font-bold">Bástyák (2db): </span>Mindkét játékos a lovakkal oldalra szomszédos
		cellákba helyezi el a bástyákat (a sarkokba).
	</p>
	<p>
		<span class="font-bold">Gyalogok (8db): </span>Mindkét játékos az eddigi bábuk elé helyezi el a
		gyalogjait.
	</p>

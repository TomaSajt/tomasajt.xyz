	<h3 class="mt-5 mb-3 text-2xl">A játék menete</h3>
	<p>
		A fehér játékos kezd, majd felváltva lépnek a játék végéig. A bábuk tudnak lépni vagy ellentétes
		színű bábut leütni.
	</p>
	<h3 class="mt-5 mb-3 text-2xl">Fugurák lépései és ütései</h3>
	<p>
		<span class="font-bold">Király: </span>Képes bármelyik 8 irányba lépni vagy ütni 1 távolságra.
		Nem tud egy másik király ütési távolságába belépni.
	</p>
	<p>
		<span class="font-bold">Királynő: </span>Képes bármelyik 8 irányba lépni vagy ütni bármilyen
		távolságra. Nem képes bábuk fölött átugrani.
	</p>
	<p>
		<span class="font-bold">Futó: </span>Képes a 4 átlós irányba lépni vagy ütni bármilyen
		távolságra. Nem képes bábuk fölött átugrani.
	</p>
	<p>
		<span class="font-bold">Huszár: </span>Lóugrásokban lép vagy üt. Vízszíntesen 2 mezőt, majd
		függőlegesen 1-et, vagy függőlegesen 2 mezőt, majd vízszíntesen 1-et. Képes bábuk fölött
		átugrani.
	</p>
	<p>
		<span class="font-bold">Bástya: </span>Képes függőlegesen vagy vízszíntesen lépni vagy ütni
		bármilyen távolságra. Nem képes bábuk fölött átugrani.
	</p>
	<p>
		<span class="font-bold">Gyalog: </span>Az ellentétes térfél felé tud lépni 1-et függőlegesen. Ha
		még nem mozdult el a kezdőpozíciójából, akkor 2-t is tud lépni előre. Nem előre, hanem csak
		előre-átlóba képes ütni.
	</p>
	<h3 class="mt-5 mb-3 text-2xl">Sakk (no way, ez a neve a játéknak)</h3>
	<p>
		Ha a király egy olyan pozícióban van, hogy az ellenség a következő körben ki tudná ütni, akkor
		sakkban van. Ilyenkor a sakkban lévő játékos köteles a királyát egy olyan pozícióba mozgatni,
		ahol nincs sakkban. Ha ez nem lehetséges, akkor vége a játéknak, sakk-matt.
	</p>
	<p>Nem lehet a királlyal olyan helyre lépni, ahol sakkban lenne.</p>
	<h3 class="mt-5 mb-3 text-2xl">Játék vége</h3>
	<p>A győzelemhez sakk-mattot kell adni az ellenfélnek.</p>
	<p>
		Ha egy játékos nincs sakkban, de bárhova lépne sakkban lenne, ez patthelyzet, ekkor döntetlen a
		játék.
	</p>
